import java.util.Scanner;
// 지각을 자주 하는 상근이가 알람을 45분 일찍 맞추려고 한다.
// 일어나고 싶은 알람 시각을 입력했을 때, 이를 언제로 고쳐야 하는 지 구하는 프로그램을 작성하시오.
// 첫째 줄에 두 정수 H와 M이 주어진다. (0 ≤ H ≤ 23, 0 ≤ M ≤ 59) 그리고 이것은 현재 상근이가 설정한 알람 시간 H시 M분을 의미한다.
//
// 입력 시간은 24시간 표현을 사용한다. 시간을 나타낼 때, 불필요한 0은 사용하지 않는다.
// 24시간 표현에서 하루의 시작은 0:0(자정)이고, 끝은 23:59(다음날 자정 1분 전)이다.
// 입력 예시 : 10 10 / 0 30 / 23 40
// 출력 예시 : 9 25 / 23 45 / 22 55

public class joon2884 {
    public static void main(String[] args) {  //
        Scanner sc = new Scanner(System.in);  //

        int h = sc.nextInt();  // 알람의 '시(hour)' 값을 정수로 입력받음 (예: 10)
        int m = sc.nextInt();  // 알람의 '분(minute)' 값을 정수로 입력받음 (예: 10)

        // 입력된 시와 분이 시간의 범위(0~23시, 0~59분)에 맞는지 확인
        if(h >= 0 && h <= 23 && m >= 0 && m <= 59) {

            // 만약 입력된 분이 45 이상이라면, 그냥 45를 빼주기만 하면 돼요
            if (m >= 45) {
                m = m - 45;  // 분에서 45를 빼서 새로운 분 계산 (예: 10분 → -35가 아니라 25분)
                // 이 경우 시(h)는 그대로 두면 됨
            } else {
                // 만약 분이 45보다 작으면, 그냥 빼면 음수가 되니까
                // 1시간을 빼고, 분은 (60분 기준)에서 보정해줘야 해요
                m = 60 + (m - 45);  // 음수가 안 되게 60분을 더한 후 빼기
                h = h - 1;          // 시(hour)에서 1시간 빼기

                // 만약 현재 시간이 0시(자정)인데 여기서 1시간을 빼면 -1시가 되니까
                // 하루의 맨 끝인 23시로 되돌려줘야 해요
                if (h < 0) {
                    h = 23;
                }
            }

            // 최종적으로 바뀐 시와 분을 출력 (예: "9 25")
            System.out.println(h + " " + m);
        }
    }
}
